<?php

namespace App\Http\Middleware;

use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;
use Inertia\Middleware;

class HandleInertiaRequests extends Middleware
{
    /**
     * The root template that's loaded on the first page visit.
     *
     * @see https://inertiajs.com/server-side-setup#root-template
     *
     * @var string
     */
    protected $rootView = 'app';

    /**
     * Determines the current asset version.
     *
     * @see https://inertiajs.com/asset-versioning
     */
    public function version(Request $request): ?string
    {
        return parent::version($request);
    }

    /**
     * Define the props that are shared by default.
     *
     * @see https://inertiajs.com/shared-data
     *
     * @return array<string, mixed>
     */
    public function share(Request $request): array
    {
        return array_merge(parent::share($request), [
            'auth' => [
                'user' => $this->getAuthenticatedUser($request),
                'guard' => $this->getCurrentGuard($request),
            ],
            'flash' => [
                'success' => $request->session()->get('success'),
                'error' => $request->session()->get('error'),
                'info' => $request->session()->get('info'),
            ],
        ]);
    }

    /**
     * Get the authenticated user for the current guard.
     */
    protected function getAuthenticatedUser(Request $request)
    {
        // Check customer guard
        if (Auth::guard('customer')->check()) {
            return Auth::guard('customer')->user();
        }

        // Check shopkeeper guard
        if (Auth::guard('shopkeeper')->check()) {
            return Auth::guard('shopkeeper')->user();
        }

        // Web guard removed - using customer and shopkeeper guards instead
        // if (Auth::guard('web')->check()) {
        //     return Auth::guard('web')->user();
        // }

        return null;
    }

    /**
     * Get the current guard name.
     */
    protected function getCurrentGuard(Request $request): ?string
    {
        if (Auth::guard('customer')->check()) {
            return 'customer';
        }

        if (Auth::guard('shopkeeper')->check()) {
            return 'shopkeeper';
        }

        // Web guard removed
        // if (Auth::guard('web')->check()) {
        //     return 'web';
        // }

        return null;
    }
}
